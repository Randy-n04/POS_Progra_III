package pos.presentation.estadisticas;

public class Rango {
    int anioDesde;
    int mesDesde;
    int anioHasta;
    int anioResta;

    public Rango(int anioDesde, int mesDesde, int anioHasta, int anioResta) {
        this.anioDesde = anioDesde;
        this.mesDesde = mesDesde;
        this.anioHasta = anioHasta;
        this.anioResta = anioResta;
    }
}
